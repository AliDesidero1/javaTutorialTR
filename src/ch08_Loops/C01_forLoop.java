package ch08_Loops;

public class C01_forLoop {
    public static void main(String[] args) {
        // TASK 33 defa java iyidir yazin .
        for (int i = 0; i < 33; i++) {
            System.out.println("Java iyidir");
        }


// Loop döngü tekrarlanan aksiyonlar için kullanilan code bloklaridir .
// int data type
// i variable name ve sartin sinirlarini belirler .
// i ++ ise her seferinde i yi bir artir demektir .


// Task 2 basamakli tek sayilari yan yana print eden code create ediniz .
        for (int i = 11; i <= 99; i += 2) {
            System.out.print(i + " ");
        }







    }
}
